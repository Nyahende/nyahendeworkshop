            <li class="typesli"  style="padding:10px;color:orangered">Broiler Chicken.
                <p class="typespara"  style="padding:10px;color:white">A broiler is any chicken (Gallus gallus domesticus) that is bred
                     and raised specifically for meat production. Most commercial broilers reach 
                     slaughter weight between four and seven weeks of age, although slower growing 
                     breeds reach slaughter weight at approximately 14 weeks of age. Typical broilers 
                     have white feathers and yellowish skin. Broiler or sometimes broiler-fryer is also
                      used sometimes to refer specifically to younger chickens under 2.0 kilograms
                       (4+1⁄2 lb), as compared with the larger roasters.
                      Due to extensive breeding selection for rapid early growth and the husbandry 
                      used to sustain this, broilers are susceptible to several welfare concerns, 
                      particularly skeletal malformation and dysfunction, skin and eye lesions and 
                      congestive heart conditions. Management of ventilation, housing, stocking density
                    and in-house procedures must be evaluated regularly to support good welfare of the
                    flock. The breeding stock (broiler-breeders) do grow to maturity but also have their 
                    own welfare concerns related to the frustration of a high feeding motivation and beak
                     trimming. Broilers are usually grown as mixed-sex flocks in large sheds under 
                     intensive conditions. </p>
