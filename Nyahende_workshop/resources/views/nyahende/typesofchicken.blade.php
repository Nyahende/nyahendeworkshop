                    light red and often fades in the sunshine. The comb is single and medium to large
                    in size; in the females it often lops over a bit. While mainly raised for meat,
                    they are also modest producers of brown eggs. Some strains lay eggs of a dark 
                    brown shell color. They are available in both standard and bantam varieties.
