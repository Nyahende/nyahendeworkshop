            <li class="typesli"  style="padding:10px;color:orangered">Kuroiler Chicken.
                <p class="typespara"  style="padding:10px;color:white">Introduced in the early 1990s, the breed was created by Vinod
                     Kapur of Kegg Farms Private, ltd., and the name is a portmanteau of Kegg and 
                     Broiler(KUROILER). Rather than all being raised in a central hatchery.The Kuroiler 
                     is a hybrid breed of chicken developed by the Keggfarms Group in Gurgaon, Haryana.
                     Kuroilers are derived from crossing either coloured broiler males with Rhode Island
                     Red females, or, White Leghorn males crossed with female Rhode Island Reds.Kuroilers, 
                     a dual-purpose breed producing meat and eggs, can live on a diet of kitchen and
                     agricultural waste, and produce around 150 eggs per year whereas native Indian hens 
                     lay only 40 per year. The meat yield per bird of Kuroilers is also greater; males weigh
                     approximately 3.5 kg (7.7 lb) and females about 2.5 kg (5.5 lb) whereas the native male 
                     bird weighs 2.5 kg (5.5 lb) and females 1.2 kg (2.6 lb). Due to its unique genetic
                     features, the Kuroiler is resistant to diseases. The kuroiler chick is a potential 
                     bio-converter of no cost agricultural, household and natural waste abundant in 
                     villages — into human protein food and substantial incomes for rural households.Kuroiler 
                     mature in about 10 weeks compared to local breeds that take several months or even up
                     to a year to mature.  </p>
