                    characteristics of early maturity, rapid full feathering, and production of large
                    brown eggs. The mature birds are a rich chestnut red, of a somewhat lighter and 
                    more even shade than the Rhode Island Reds. The chicks are also a lighter red.They
                    possess
                    a deep, broad body, grow feathers very rapidly, are prone to go broody and make
                    good mothers. Most pin feathers are a reddish, brownish buff in color and, therefore
