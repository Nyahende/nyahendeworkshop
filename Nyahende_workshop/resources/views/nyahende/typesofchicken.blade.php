            <li class="typesli"  style="padding:10px;color:orangered">African Local Chicken.
                <p class="typespara"  style="padding:10px;color:white">There has been a research gap in the genetic, physiological,
                     and nutritional aspects of indigenous chickens of Africa over the past decade.
                     These chickens are known to be economically, socially, and culturally important
                     to the people of Africa, especially those from marginalised communities.
                     Although they are associated with poor productivity in terms of the number of
                     eggs laid, most consumers prefer their flavoursome meat. Several local chickens 
                     have been classified into breeds or ecotypes, but many remain unidentified and 
                     are facing extinction.It is believed that the local chickens of Africa originated 
                     from South-East Asia, China, and India . Local chickens are hardy and can
                     adapt to local conditions better than other breeds because of their ability to 
                     withstand harsh climatic conditions due to their typical genetic development .
                     Further to this, they also possess a strong ability to anticipate danger and act quickly.
                     Alders and Spradbrow  stated that local chickens have a strong ability to fly and run 
                     to escape dangers and predators in comparison with commercial chickens. These traits make 
                     them strong enough to survive and produce in an unfavourable environment.
                    Local African Chicken are considered to be fully matured and start to lay eggs when they are 
                around 18-24 weeks old.</p>
